def calculate_perplexity(model, input_ids, past_key_values=None):
    """Calcula a perplexity do texto (lower is better)."""
    with torch.no_grad():
        if past_key_values is None:
            outputs = model(input_ids, labels=input_ids)
        else:
            # Temos que passar context+1 no passado? Para medir perplexity com KV injetado
            # É complexo injetar todo o KV num único forward. 
            # Aproximação: medir loss dos logits reconstruídos
            pass
            
        return math.exp(outputs.loss.item())

# Para medir perplexity COM o KV cache quantizado, a forma mais robusta é injetar o KV customizado
# e gerar tokens. Mas a perplexity padrão do HuggingFace (labels=input_ids) ignora past_key_values.
# Em vez disso, vamos medir a Cosine Similarity entre a predição original e a quantizada no token final!

def compare_logits(prompt, kv_original, kv_quantized):
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
    
    # O prompt tem seq_len tokens. O KV Cache tem seq_len. 
    # Queremos gerar o token seq_len+1 usando o Cache e passando apenas o ÚLTIMO token como input
    last_token = inputs.input_ids[:, -1:]
    
    # Suporte para converter tupla legacy de volta para DynamicCache
    def prepare_cache(legacy_kv):
        try:
            from transformers.cache_utils import DynamicCache
            new_cache = DynamicCache()
            for layer_idx, (k, v) in enumerate(legacy_kv):
                new_cache.update(k, v, layer_idx)
            return new_cache
        except ImportError:
            # Versão antiga do Transformers não tem DynamicCache
            return legacy_kv
        
    kv_orig_obj = prepare_cache(kv_original)
    kv_quant_obj = prepare_cache(kv_quantized)
    
    with torch.no_grad():
        out_orig = model(last_token, past_key_values=kv_orig_obj)
        logits_orig = out_orig.logits[:, -1, :]
        
        out_quant = model(last_token, past_key_values=kv_quant_obj)
        logits_quant = out_quant.logits[:, -1, :]
    
    # Cosine Similarity dos logits (como a distribuição de probabilidade foi afetada)
    cos_sim = nn.functional.cosine_similarity(logits_orig, logits_quant).item()
    
    # L2 Error (MSE)
    mse = nn.functional.mse_loss(logits_orig, logits_quant).item()
    
    # KL Divergence entre as distribuições (Softmax)
    prob_orig = nn.functional.softmax(logits_orig, dim=-1)
    log_prob_quant = nn.functional.log_softmax(logits_quant, dim=-1)
    kl_div = nn.functional.kl_div(log_prob_quant, prob_orig, reduction='batchmean').item()
    
    return cos_sim, mse, kl_div

