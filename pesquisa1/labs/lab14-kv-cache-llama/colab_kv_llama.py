"""## 2. Extração do KV Cache Original"""

def get_kv_cache(prompt, max_tokens=256):
    """Roda o modelo num prompt e captura o past_key_values."""
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
    
    with torch.no_grad():
        # Apenas forward pass preenchendo o cache
        outputs = model(**inputs, use_cache=True)
    
    kv_cache = outputs.past_key_values
    
    # 1. Tupla nativa (versões velhas)
    if isinstance(kv_cache, tuple):
        return kv_cache
        
    # 2. Iterável (suporte __iter__ no DynamicCache v4.38+)
    try:
        extracted = list(kv_cache)
        if len(extracted) > 0 and isinstance(extracted[0], tuple):
            return tuple(extracted)
    except Exception:
        pass
        
    # 3. Método oficial de conversão
    if hasattr(kv_cache, "to_legacy_cache"):
        try:
            legacy = kv_cache.to_legacy_cache()
            if isinstance(legacy, tuple):
                return legacy
        except Exception:
            pass
            
    # 4. Nuclear Option: introspecção direta no __dict__ do objeto
    keys, values = [], []
    for attr, val in kv_cache.__dict__.items():
        # Busca por listas de tensores (que representam as camadas)
        if isinstance(val, list) and len(val) > 0 and isinstance(val[0], torch.Tensor):
            if 'key' in attr.lower() or 'k_cache' in attr.lower():
                keys = val
            elif 'value' in attr.lower() or 'v_cache' in attr.lower():
                values = val
                
    if len(keys) > 0 and len(keys) == len(values):
        return tuple(zip(keys, values))
        
    raise ValueError(f"Blindagem de extração falhou. Atributos ocultos: {kv_cache.__dict__.keys()}")

