"""## 3. Vector Quantization (Codebook Learning)"""

def quantize_tensor_vq(tensor, k=256):
    """Aplica K-Means VQ num tensor 4D do KV Cache."""
    # tensor shape: (batch, heads, seq, dim)
    b, h, s, d = tensor.shape
    
    # Achatar para 2D: (batch*heads*seq, dim)
    flat_tensor = tensor.reshape(-1, d).float().cpu().numpy()
    
    # Treinar Codebook
    kmeans = MiniBatchKMeans(n_clusters=k, random_state=42, batch_size=1024, n_init=3)
    labels = kmeans.fit_predict(flat_tensor)
    
    # Reconstruir tensor quantizado
    centroids = kmeans.cluster_centers_
    quantized_flat = centroids[labels]
    
    # Voltar para shape e device originais
    quantized_tensor = torch.tensor(quantized_flat, dtype=tensor.dtype, device=tensor.device)
    quantized_tensor = quantized_tensor.reshape(b, h, s, d)
    
    # Calcular compressão
    # Tamanho original: N * d * 2 bytes (fp16)
    # Codebook: K * d * 2 bytes + N * (log2(K)/8) bytes (indices)
    orig_bytes = flat_tensor.shape[0] * d * 2
    codebook_bytes = k * d * 2
    
    # 256 = 1 byte per index (8 bits)
    # 65536 = 2 bytes per index (16 bits)
    bytes_per_idx = math.ceil(math.log2(k)/8)
    indices_bytes = flat_tensor.shape[0] * bytes_per_idx
    
    comp_bytes = codebook_bytes + indices_bytes
    ratio = 1.0 - (comp_bytes / orig_bytes)
    
    return quantized_tensor, ratio, comp_bytes

